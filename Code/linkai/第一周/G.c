#include<stdio.h>
int main()
{
	int d1,d2,d3,t;
	scanf("%d%d%d",&d1,&d2,&d3);
	t=d1+d2+d3;
	if(t>2*(d1+d2)) t=2*(d1+d2);
	if(t>2*(d1+d3)) t=2*(d1+d3);
	if(t>2*(d3+d2)) t=2*(d3+d2);
	printf("%d\n",t);
}
